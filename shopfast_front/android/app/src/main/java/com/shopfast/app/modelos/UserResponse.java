package com.shopfast.app.modelos;

public class UserResponse {
    private String id;
    private String username;
    private String email;
    private String created_at;

    public String getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }

    public String getCreated_at() {
        return created_at;
    }
}
